# Exercise 02: Variables and Data Types

# String variable
name = "Python Learner"
print(f"Hello, {name}!")

# Integer variable
age = 25
print(f"Age: {age}")

# Float variable
height = 175.5
print(f"Height: {height} cm")

# Boolean variable
is_student = True
print(f"Is student: {is_student}")

# List variable
hobbies = ["programming", "reading", "music"]
print(f"Hobbies: {hobbies}")
